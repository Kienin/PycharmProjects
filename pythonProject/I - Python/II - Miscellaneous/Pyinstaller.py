# Pyinstaller: a tool that converts (bundles) I - Python programs into standalone executables.
#              This means that instead of requiring users to install I - Python and the necessary
#              dependencies to run a I - Python script, you can package everything into a single executable
#              file that can be run on a target system without needing a separate I - Python interpreter or
#              additional installations.

# To Execute:

# 1. Open Terminal
# 2. Type:
#         pyinstaller --distpath C:\Users\kevin\OneDrive\Documents\Executables programname.py --noconsole
# 3. Exe file should open to the Executables Folder on your Documents in the file manager
